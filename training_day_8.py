class Solution:
    def middleNode(self, head: Optional[ListNode]) -> Optional[ListNode]:
        n = 0
        temp = head
        # Count nodes
        while temp:
            n += 1
            temp = temp.next
        # Move to middle
        mid = n // 2
        temp = head
        for _ in range(mid):
            temp = temp.next
        return temp

